function WyrmprintBuild(PrintID, Buildup, Augments, UserIndexRecord) {
	const PrintIndex = UserIndexRecord['ability_crest_list'].findIndex(x => x.ability_crest_id === PrintID);
	const PreviousWyrmprintData = UserIndexRecord['ability_crest_list'][PrintIndex];
	const PrintData = WyrmprintInfoMap[String(PrintID)];
	let UpdateList = [];
	var Template = {
        "ability_crest_id": PrintID,
        "buildup_count": PreviousWyrmprintData['buildup_count'],
        "limit_break_count": PreviousWyrmprintData['limit_break_count'],
        "equipable_count": PreviousWyrmprintData['equipable_count'],
        "hp_plus_count": PreviousWyrmprintData['hp_plus_count'],
        "attack_plus_count": PreviousWyrmprintData['attack_plus_count'],
        "is_new": 0,
        "is_favorite": PreviousWyrmprintData['is_favorite'],
        "gettime": PreviousWyrmprintData['gettime']
	}
	
	if (Buildup != undefined) {
	for (const i in Buildup) {
		let MaterialList = [];
		switch(Buildup[i]['buildup_piece_type']) {
			case 1: // Unbind
				UserIndexRecord['user_data']['dew_point'] -= WyrmprintBuildMap[String(PrintData['build_group'])][Buildup[i]['buildup_piece_type']][Buildup[i]['step']]['dew_cost'];
				MaterialList = WyrmprintBuildMap[String(PrintData['build_group'])][Buildup[i]['buildup_piece_type']][Buildup[i]['step']]['material_cost'];
				if (WyrmprintBuildMap[String(PrintData['build_group'])][Buildup[i]['buildup_piece_type']][Buildup[i]['step']]['unique_cost'] != 0) {
					if (Buildup[i]['is_use_dedicated_material'] == 1) {
						const Rarity = WyrmprintInfoMap[String(PrintID)]['rarity'];
						let UpgradeItem = 114001001;
						if (Rarity == 5) { UpgradeItem = 114002001; }
						
						const UpgradeItemIndex = MaterialList.findIndex(x => x.id == UpgradeItem);
						if (UpgradeItemIndex == -1) { MaterialList.push({ 'id': UpgradeItem, 'quantity': 1 }); }
						else { MaterialList[UpgradeItemIndex]['quantity'] += 1; }
					}
					else {
						MaterialList.push({
							'id': PrintData['unique_material'],
							'quantity': WyrmprintBuildMap[String(PrintData['build_group'])][Buildup[i]['buildup_piece_type']][Buildup[i]['step']]['unique_cost']
						});
					}
				}
				Template['limit_break_count'] = Buildup[i]['step'];
				break;
			case 6: // Copies
				UserIndexRecord['user_data']['dew_point'] -= WyrmprintBuildMap[String(PrintData['build_group'])][Buildup[i]['buildup_piece_type']][Buildup[i]['step']]['dew_cost'];
				MaterialList = WyrmprintBuildMap[String(PrintData['build_group'])][Buildup[i]['buildup_piece_type']][Buildup[i]['step']]['material_cost'];
				if (WyrmprintBuildMap[String(PrintData['build_group'])][Buildup[i]['buildup_piece_type']][Buildup[i]['step']]['unique_cost'] != 0) {
					MaterialList.push({
						'id': PrintData['unique_material'],
						'quantity': WyrmprintBuildMap[String(PrintData['build_group'])][Buildup[i]['buildup_piece_type']][Buildup[i]['step']]['unique_cost']
					});
				}
				Template['equipable_count'] = Buildup[i]['step'];
				break;
			case 7: // Level Up
				MaterialList = WyrmprintLevelMap[String(PrintData['rarity'])][String(Buildup[i]['step'])]['material_cost'];
				if (WyrmprintLevelMap[String(PrintData['rarity'])][String(Buildup[i]['step'])]['unique_cost'] != 0) {
					MaterialList.push({
						'id': PrintData['unique_material'],
						'quantity': WyrmprintLevelMap[String(PrintData['rarity'])][String(Buildup[i]['step'])]['unique_cost']
					});
				}
				Template['buildup_count'] = Buildup[i]['step'];
				break;
		}
		
		for (const m in MaterialList) {
			const MatIndex = UserIndexRecord['material_list'].findIndex(x => x.material_id == MaterialList[m]['id']);
			const UpdateMatIndex = UpdateList.findIndex(x => x.material_id == MaterialList[m]['id']);
			UserIndexRecord['material_list'][MatIndex]['quantity'] -= MaterialList[m]['quantity'];
			if (UpdateMatIndex != -1) { UpdateList[UpdateMatIndex]['quantity'] = UserIndexRecord['material_list'][MatIndex]['quantity']; }
			else { UpdateList.push(UserIndexRecord['material_list'][MatIndex]); }
		}
		
	}}
	if (Augments != undefined) {
	for (const y in Augments) {
		let MatIndex = -1;
		switch(Augments[y]['plus_count_type']) {
			case 1:
				MatIndex = UserIndexRecord['material_list'].findIndex(x => x.material_id == 122001001);
				Template['hp_plus_count'] = Augments[y]['plus_count'];
				break;
			case 2:
				MatIndex = UserIndexRecord['material_list'].findIndex(x => x.material_id == 123001001);
				Template['attack_plus_count'] = Augments[y]['plus_count'];
				break;
		}
		UserIndexRecord['material_list'][MatIndex]['quantity'] -= Augments[y]['plus_count'];
		UpdateList.push(UserIndexRecord['material_list'][MatIndex]);
	}}
	
	UserIndexRecord['ability_crest_list'][PrintIndex] = Template;
	return [Template, UpdateList, UserIndexRecord];
}
