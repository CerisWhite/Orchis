function GetWyrmprintInfo(WyrmprintID, Attribute) {
	if (WyrmprintInfoMap[String(WyrmprintID)] == undefined) { return 0; }
	return WyrmprintInfoMap[String(WyrmprintID)][Attribute];
}

function WyrmprintIDByName(WyrmprintName) {
	const List = Object.keys(WyrmprintInfoMap);
	for (const i in List) {
		if (WyrmprintInfoMap[List[i]]['name'] == WyrmprintName) {
			return parseInt(List[i]);
		}
	}
	return 0;
}

