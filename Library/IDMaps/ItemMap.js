function GetItemInfo(ItemID, Attribute) {
	return ItemInfoMap[String(ItemID)][Attribute];
}

function ItemIDByName(ItemName) {
	let i = 0;
	while (i < Object.keys(ItemInfoMap).length) {
		const ItemID = Object.keys(ItemInfoMap)[i];
		if (ItemInfoMap[ItemID]['name'] == ItemName) {
			return parseInt(Object.keys(ItemInfoMap)[i]);
		}
		else {
			i++;
		}
	}
	return 1;
}

function GetRandomItem() {
	const RandomItem = Math.floor(Math.random() * Object.keys(ItemInfoMap).length);
	return parseInt(Object.keys(ItemInfoMap)[RandomItem]);
}

function GetRarityMap(Rarity) {
	let i = 0;
	let RarityMap = [];
	while (i < Object.keys(ItemInfoMap).length) {
		const ItemID = Object.keys(ItemInfoMap)[i];
		if (ItemInfoMap[ItemID]['rarity'] == Rarity) {
			RarityMap.push(ItemID);
			i++;
		}
		else { i++; }
	}
	return RarityMap;
}

